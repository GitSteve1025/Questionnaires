<script setup>

import {Lock, User,Message,Bell} from "@element-plus/icons-vue";
import router from "@/router";
import {reactive,ref} from "vue";
import {post} from "@/net"
import {ElMessage} from "element-plus";

const form=reactive({
  username:'',
  password:'',
  password_repeat:'',
  email:'',
  code:''
})

const validateUsername = (rule, value, callback) => {
  if (value === '') {
    callback(new Error('请输入用户名'))
  } else if(!/^[a-zA-Z0-9\u4e00-\u9fa5]+$/.test(value)){
    callback(new Error('用户名不能包含特殊字符，只能是中/英文'))
  } else {
    callback()
  }
}

const validatePassword = (rule, value, callback) => {
  if (value === '') {
    callback(new Error('请再次输入密码'))
  } else if (value !== form.password) {
    callback(new Error("两次输入的密码不一致"))
  } else {
    callback()
  }
}

const rules = {
  username: [
    { validator: validateUsername, trigger: ['blur', 'change'] },
    { min: 2, max: 10, message: '用户名的长度必须在2-10个字符之间', trigger: ['blur', 'change'] },
  ],
  password: [
    { required: true, message: '请输入密码', trigger: 'blur' },
    { min: 3, max: 16, message: '密码的长度必须在3-16个字符之间', trigger: ['blur', 'change'] }
  ],
  password_repeat: [
    { validator: validatePassword, trigger: ['blur', 'change'] },
  ],
  email: [
    { required: true, message: '请输入邮件地址', trigger: 'blur' },
    {type: 'email', message: '请输入合法的电子邮件地址', trigger: ['blur', 'change']}
  ],
  code: [
    { required: true, message: '请输入获取的验证码', trigger: 'blur' },
  ]
}

const formRef = ref()
const isEmailValid = ref(false)

const onValidate = (prop, isValid) => {
  if(prop === 'email')
    isEmailValid.value = isValid
}

const register = () => {
  formRef.value.validate((isValid) => {
    if(isValid) {
      post('/api/auth/register', {
        username: form.username,
        password: form.password,
        email: form.email,
        code: form.code
      }, (message) => {
        ElMessage.success(message)
        router.push("/")
      })
    } else {
      ElMessage.warning('请完整填写注册表单内容！')
    }
  })
}
</script>

<template>
  <div style="margin-top:100px">
    <div style="font-size:25px;font-weight:bold"> 注册新用户</div>
    <div style="font-size: 14px;color:gray">请在下方填写相关信息</div>
  </div>
  <div style="margin-top:50px">
    <el-form :model="form" :rules="rules" @validate="onValidate" ref="formRef">
      <el-form-item prop="username">
        <el-input v-model="form.username" :maxlength="10" type="text" placeholder="用户名">
          <template #prefix>
            <el-icon><User /></el-icon>
          </template>
        </el-input>
      </el-form-item>

      <el-form-item prop="password">
        <el-input v-model="form.password" :maxlength="16" type="password" placeholder="密码" >
          <template #prefix>
            <el-icon><Lock /></el-icon>
          </template>
        </el-input>
      </el-form-item>

      <el-form-item prop="password_repeat">
        <el-input v-model="form.password_repeat":maxlength="16" type="password" placeholder="重置密码" >
          <template #prefix>
            <el-icon><Lock /></el-icon>
          </template>
        </el-input>
      </el-form-item>
      <el-form-item prop="email">
        <el-input v-model="form.email" type="email" placeholder="电子邮箱" >
          <template #prefix>
            <el-icon><Message /></el-icon>
          </template>
        </el-input>
      </el-form-item>

      <el-form-item prop="code">
        <el-row :gutter="10" style="width:100%">
          <el-col :span="18">
            <el-input v-model="form.code":maxlength="6" type="text" placeholder="请输入验证码">
              <template #prefix>
                <el-icon><Bell /></el-icon>
              </template>
            </el-input>
          </el-col>
          <el-col :span="6">
            <el-button type="success" :disabled="!isEmailValid">获取验证码</el-button>
          </el-col>
        </el-row>
      </el-form-item>
    </el-form>
  </div>
  <div style="margin-top:10px">
    <div style="margin-top:100px">
      <el-button style="width:100px" type="warning" @click="register" >
        立即注册
      </el-button>
    </div>

    <div style="font-size:14px;line-height: 15px;margin-top: 20px">
      <span style="color:grey">已有账号？</span>
      <el-link type="primary" style="translate: 0 -2px" @click="router.push('/')">立即登录</el-link>
    </div>

  </div>
</template>

